# esta funcion es para que la tierra parezca la tierra y no una bola azul
# no afecta en nada a la simulacion

def crear_textura_tierra(resolucion=100):
    u = np.linspace(0, 2 * np.pi, resolucion)
    v = np.linspace(0, np.pi, resolucion)
    U, V = np.meshgrid(u, v)

    # se convierte a coordenadas lat/lon
    latitud = 90 - np.degrees(V)  # Latitud: 90 (polo norte) a -90 (polo sur)
    longitud = np.degrees(U)  # Longitud: 0 a 360

    # oceanos
    colores = np.ones((resolucion, resolucion, 3))
    colores[:, :, 0] = 0.1
    colores[:, :, 1] = 0.3
    colores[:, :, 2] = 0.6

    # aqui se hace una mascara de los continentes
    mascara_america = (
        ((longitud > 190) & (longitud < 290) & (latitud > 10) & (latitud < 70)) |  # Norte America
        ((longitud > 210) & (longitud < 280) & (latitud > -55) & (latitud < 15))    # Sur America
    )

    mascara_europa = (
        (longitud > 350) | (longitud < 40)
    ) & (latitud > 35) & (latitud < 70)

    mascara_africa = (
        (longitud > 345) | (longitud < 50)
    ) & (latitud > -35) & (latitud < 38)

    mascara_asia = (
        ((longitud > 40) & (longitud < 150) & (latitud > 10) & (latitud < 75)) |  # Asia continental
        ((longitud > 90) & (longitud < 120) & (latitud > -10) & (latitud < 25))    # Sudeste asiatico
    )

    mascara_australia = (
        (longitud > 110) & (longitud < 155) & (latitud > -45) & (latitud < -10)
    )

    mascara_antartida = (latitud < -60)

    mascara_tierra = (mascara_america | mascara_europa | mascara_africa |
                      mascara_asia | mascara_australia | mascara_antartida)

    # aqui les pongo color
    colores[mascara_tierra, 0] = 0.2 + 0.2 * np.random.rand(np.sum(mascara_tierra))
    colores[mascara_tierra, 1] = 0.5 + 0.2 * np.random.rand(np.sum(mascara_tierra))
    colores[mascara_tierra, 2] = 0.1 + 0.1 * np.random.rand(np.sum(mascara_tierra))

    colores[mascara_antartida, 0] = 0.9
    colores[mascara_antartida, 1] = 0.95
    colores[mascara_antartida, 2] = 1.0

    # aqui se agregan nubes
    mascara_nubes = np.random.rand(resolucion, resolucion) > 0.7
    intensidad_nubes = np.random.rand(resolucion, resolucion) * 0.3

    colores[mascara_nubes] = colores[mascara_nubes] + intensidad_nubes[mascara_nubes][:, np.newaxis]
    colores = np.clip(colores, 0, 1)

    return colores


