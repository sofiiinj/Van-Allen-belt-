def dipolo_magnetico(x, tau):
    """
    X, Y, Z = posiciones normalizadas
    dX, dY, dZ = velocidades normalizadas
    τ  = tiempo normalizado
    """
    X, dX, Y, dY, Z, dZ = x #vector que tiene la posicion y velocidad en cada eje

    r2 = X**2 + Y**2 + Z**2 #r^2, distancia al cuadrado desde el origen

    r5 = r2**(5/2)

    # ecuaciones diferenciales que describen las aceleraciones derivadas de la fuerza de Lorentz
    d2X = ((2*Z**2 - Y**2 - X**2)*dY - 3*Y*Z*dZ) / r5
    d2Y = -((2*Z**2 - Y**2 - X**2)*dX - 3*X*Z*dZ) / r5
    d2Z = 3*Z*(Y*dX - X*dY) / r5

    return [dX, d2X, dY, d2Y, dZ, d2Z]

