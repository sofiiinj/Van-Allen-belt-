class SimulacionCinturonVanAllen:
    def __init__(self):
        self.radio_tierra = 1.0  # radio terrestre normalizado para simplificar
        self.radio_cinturon_interno = 1.5 * self.radio_tierra  # cinturon interno (protones)
        self.radio_cinturon_externo = 4.0 * self.radio_tierra  # cinturon externo (electrones)

    def generar_trayectoria_espiral(self, tipo_particula, radio_cinturon, duracion=100):

        # se definen distintos parametros para la espiral segun el tipo de particula
        if tipo_particula == 'electron':
            radio_helice = 0.15  # radio de la espiral
            velocidad_rotacion = 8.0  # velocidad angular
            velocidad_rebote = 0.5   # velocidad de rebote
            velocidad_deriva = 0.015  # velocidad de deriva
            color = 'red'
            alfa = 0.8
        else:  # solo puede ser proton
            radio_helice = 0.25
            velocidad_rotacion = 3.0
            velocidad_rebote = 0.3
            velocidad_deriva = 0.008
            color = 'cyan'
            alfa = 0.8

        # Angulo inicial aleatorio
        theta0 = np.random.uniform(0, 2*np.pi)
        phi0 = np.random.uniform(-0.3, 0.3)  # se define un angulo inicial cerca del ecuador

        # con este parametro se determinan las lineas de campo magnetico
        parametro_L = radio_cinturon / self.radio_tierra

        # array de tiempo
        tiempo = np.linspace(0, duracion, 400)

        # aqui se define el movimiento de rebote, que hace que la particula oscile entre norte y sur
        lambda_max = np.arccos(np.sqrt(1/parametro_L)) * 0.85  # este es el angulo maximo de latitud
        movimiento_rebote = lambda_max * np.sin(velocidad_rebote * tiempo) # esta es la ecuacion de movimiento

        # aqui se define el movimiento de deriva, que hace que la particula se mueva longitudinalmente
        theta_deriva = theta0 + velocidad_deriva * tiempo

        # aqui se definen las coordenadas del centro de la espiral con coordenadas esfericas para las lineas de campo
        phi_centro = np.pi/2 - movimiento_rebote  # complemento de la latitud
        radio_centro = parametro_L * np.cos(movimiento_rebote)**2  # radio en funcion de la latitud

        # aqui se define el centro de la espiral en cartesianas
        x_centro = radio_centro * np.sin(phi_centro) * np.cos(theta_deriva)
        y_centro = radio_centro * np.sin(phi_centro) * np.sin(theta_deriva)
        z_centro = radio_centro * np.cos(phi_centro)

        # aqui se agrega el angulo de rotacion por la velocidad angular
        angulo_espiral = velocidad_rotacion * tiempo

        # aqui se definen los distintos vectores que hace que se cree la trayectoria espiral
        # se define un vector tangente con el gradiente
        dx = np.gradient(x_centro)
        dy = np.gradient(y_centro)
        dz = np.gradient(z_centro)
        norma = np.sqrt(dx**2 + dy**2 + dz**2) + 1e-10
        dx, dy, dz = dx/norma, dy/norma, dz/norma

        # se define un primer vector perpendicular arbitrario
        perp1_x = -dy
        perp1_y = dx
        perp1_z = np.zeros_like(dx)
        norma1 = np.sqrt(perp1_x**2 + perp1_y**2 + perp1_z**2) + 1e-10
        perp1_x, perp1_y, perp1_z = perp1_x/norma1, perp1_y/norma1, perp1_z/norma1

        # un segundo vector perpendicular con el producto cruz de los primeros dos
        perp2_x = dy * perp1_z - dz * perp1_y
        perp2_y = dz * perp1_x - dx * perp1_z
        perp2_z = dx * perp1_y - dy * perp1_x

        # esta ya seria la posicion final de la espiral
        x_espiral = x_centro + radio_helice * (np.cos(angulo_espiral) * perp1_x +
                                               np.sin(angulo_espiral) * perp2_x)
        y_espiral = y_centro + radio_helice * (np.cos(angulo_espiral) * perp1_y +
                                               np.sin(angulo_espiral) * perp2_y)
        z_espiral = z_centro + radio_helice * (np.cos(angulo_espiral) * perp1_z +
                                               np.sin(angulo_espiral) * perp2_z)

        return {
            'x': x_espiral,
            'y': y_espiral,
            'z': z_espiral,
            'color': color,
            'alfa': alfa,
            'radio_helice': radio_helice
        }


