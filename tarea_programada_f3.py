def campo_no_uniforme(x, tau, D):
    X, Y, dY, Z, dZ = x

    dX = (1 + Z)*Y + D
    d2Y = -(1 + Z)**2 * Y - D*(1 + Z)
    d2Z = -(1 + Z)*Y**2 - D*Y

    return [dX, dY, d2Y, dZ, d2Z]

