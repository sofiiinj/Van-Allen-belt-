def simulacion_van_allen():
    tiempo_inicio = time.time()

    # se crea el simulador
    simulacion = SimulacionCinturonVanAllen()

    # se generan las particulas
    num_electrones = 4
    num_protones = 4

    # aqui se generan las trayectorias para cada una de las particulas
    trayectorias_electrones = [
        simulacion.generar_trayectoria_espiral('electron', simulacion.radio_cinturon_externo, duracion=100)
        for _ in range(num_electrones)
    ]
    trayectorias_protones = [
        simulacion.generar_trayectoria_espiral('proton', simulacion.radio_cinturon_interno, duracion=100)
        for _ in range(num_protones)
    ]

    # aqui se empieza a generar la figura
    figura = plt.figure(figsize=(16, 12))
    eje = figura.add_subplot(111, projection='3d')

    # aqui se crea la tierra y se le pone la textura que hice al puro principio
    resolucion = 100
    u = np.linspace(0, 2 * np.pi, resolucion)
    v = np.linspace(0, np.pi, resolucion)

    x_tierra = simulacion.radio_tierra * np.outer(np.cos(u), np.sin(v))
    y_tierra = simulacion.radio_tierra * np.outer(np.sin(u), np.sin(v))
    z_tierra = simulacion.radio_tierra * np.outer(np.ones(np.size(u)), np.cos(v))
    colores_tierra = crear_textura_tierra(resolucion)
    eje.plot_surface(x_tierra, y_tierra, z_tierra,
                   facecolors=colores_tierra,
                   shade=True,
                   alpha=0.95,
                   antialiased=True)

    # aqui se dibujan las lineas de campo magnetico

    def lineas_campo_magnetico(eje, num_lineas=8):
        # se definen distintos valores del parametro L para crear distintas lineas
        valores_L = np.linspace(1.5, 5.0, num_lineas)

        for L in valores_L:
            # este es el angulo de latitud magnetica
            lambda_magnetico = np.linspace(-np.pi/2 + 0.1, np.pi/2 - 0.1, 100)

            # este es el radio en funcion de la latitud
            radio = L * np.cos(lambda_magnetico)**2

            # aqui se dibuja la linea en diferentes planos longitudinales
            for theta in [0, np.pi/2, np.pi, 3*np.pi/2]:
                # se pasa a coordenadas cartesianas
                phi = np.pi/2 - lambda_magnetico
                x_campo = radio * np.sin(phi) * np.cos(theta)
                y_campo = radio * np.sin(phi) * np.sin(theta)
                z_campo = radio * np.cos(phi)

                # aqui ya se dibuja la linea
                eje.plot(x_campo, y_campo, z_campo,
                       color='lime',
                       alpha=0.3,
                       linewidth=1.0,
                       linestyle='--')

    lineas_campo_magnetico(eje, num_lineas=6)

    # aqui se definen los objetos necesarios para los electrones
    puntos_electrones = []
    lineas_electrones = []
    for _ in trayectorias_electrones:
        punto = eje.scatter([], [], [], c='red', s=80, marker='o',
                           edgecolors='yellow', linewidths=1.5, alpha=1.0)
        linea, = eje.plot([], [], [], 'red', alpha=0.6, linewidth=2.5)
        puntos_electrones.append(punto)
        lineas_electrones.append(linea)

    # aqui se definen los objetos necesarios para los protones
    puntos_protones = []
    lineas_protones = []
    for _ in trayectorias_protones:
        punto = eje.scatter([], [], [], c='cyan', s=100, marker='o',
                           edgecolors='blue', linewidths=1.5, alpha=1.0)
        linea, = eje.plot([], [], [], 'cyan', alpha=0.6, linewidth=2.5)
        puntos_protones.append(punto)
        lineas_protones.append(linea)

    # ejes, fondo, leyenda, titulos, etc
    radio_maximo = simulacion.radio_cinturon_externo * 1.2
    eje.set_xlim([-radio_maximo, radio_maximo])
    eje.set_ylim([-radio_maximo, radio_maximo])
    eje.set_zlim([-radio_maximo, radio_maximo])
    eje.set_xlabel('X (Radios Terrestres)', fontsize=11, fontweight='bold')
    eje.set_ylabel('Y (Radios Terrestres)', fontsize=11, fontweight='bold')
    eje.set_zlabel('Z (Radios Terrestres)', fontsize=11, fontweight='bold')
    eje.set_title('CINTURONES DE VAN ALLEN\n', fontsize=13, fontweight='bold', pad=20)
    eje.xaxis.pane.fill = False
    eje.yaxis.pane.fill = False
    eje.zaxis.pane.fill = False
    eje.set_facecolor('black')
    figura.patch.set_facecolor('black')
    eje.xaxis.label.set_color('white')
    eje.yaxis.label.set_color('white')
    eje.zaxis.label.set_color('white')
    eje.tick_params(colors='white')
    eje.title.set_color('white')

    from matplotlib.lines import Line2D
    elementos_leyenda = [
        Line2D([0], [0], marker='o', color='w', label='Electrones',
               markerfacecolor='red', markersize=8),
        Line2D([0], [0], marker='o', color='w', label='Protones',
               markerfacecolor='cyan', markersize=8),
        Line2D([0], [0], color='lime', linewidth=2, linestyle='--',
               label='Campo magnetico')
    ]
    eje.legend(handles=elementos_leyenda,
              loc='upper left',
              fontsize=10,
              framealpha=0.8)

    # aqui ya se empieza a definir la animacion
    def inicializar():
        for punto in puntos_electrones + puntos_protones:
            punto._offsets3d = ([], [], [])
        for linea in lineas_electrones + lineas_protones:
            linea.set_data([], [])
            linea.set_3d_properties([])
        return puntos_electrones + puntos_protones + lineas_electrones + lineas_protones

    # aqui es donde se actualizan los frames
    def animar(cuadro):
        longitud_estela = 80

        # aqui se actualizan los electrones
        for i, trayectoria in enumerate(trayectorias_electrones):
            indice = cuadro % len(trayectoria['x'])
            puntos_electrones[i]._offsets3d = (
                [trayectoria['x'][indice]],
                [trayectoria['y'][indice]],
                [trayectoria['z'][indice]]
            )
            indice_inicio = max(0, indice - longitud_estela)
            lineas_electrones[i].set_data(
                trayectoria['x'][indice_inicio:indice+1],
                trayectoria['y'][indice_inicio:indice+1]
            )
            lineas_electrones[i].set_3d_properties(trayectoria['z'][indice_inicio:indice+1])

        # aqui se actualizan los protones
        for i, trayectoria in enumerate(trayectorias_protones):
            indice = cuadro % len(trayectoria['x'])
            puntos_protones[i]._offsets3d = (
                [trayectoria['x'][indice]],
                [trayectoria['y'][indice]],
                [trayectoria['z'][indice]]
            )
            indice_inicio = max(0, indice - longitud_estela)
            lineas_protones[i].set_data(
                trayectoria['x'][indice_inicio:indice+1],
                trayectoria['y'][indice_inicio:indice+1]
            )
            lineas_protones[i].set_3d_properties(trayectoria['z'][indice_inicio:indice+1])

        # esto es para rotar la perspectiva para que se vean todas las simulaciones
        eje.view_init(elev=20, azim=cuadro * 0.5)

        return puntos_electrones + puntos_protones + lineas_electrones + lineas_protones

    # aqui ya se hace la animacion como tal
    num_cuadros = 400
    animacion = FuncAnimation(
        figura,
        animar,
        init_func=inicializar,
        frames=num_cuadros,
        interval=50,
        blit=False,
        repeat=True
    )

    tiempo_final = time.time()
    print(f"Simulacion hecha en {tiempo_final - tiempo_inicio:.2f} segundos")
    try:
        animacion.save(
            'SimulacionVanAllen.gif',
            writer='pillow',
            fps=20,
            dpi=100
        )
        print("Animacion guardada")

    except Exception as e:
        print(f"no se pudo guardo animacion")
    plt.tight_layout()
    plt.show()

